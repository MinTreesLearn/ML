ll n, k, m, a, b, hp[200005];


void query(){
    cin >> n >> a >> b >> k;

    for (ll i = 1; i <= n; i++){
        cin >> hp[i];

        hp[i] %= (a + b);

        if (!hp[i]) hp[i] += a + b;

        if (hp[i] % a){
            hp[i] = hp[i] / a;
        }

        else hp[i] = hp[i] / a - 1;
    }

    ll ans = 0;

    sort (hp + 1, hp + n + 1);

    for (ll i = 1; i <= n; i++){
        if (hp[i] > k) break;

        ans++;

        k -= hp[i];
    }

    cout << ans;
}

