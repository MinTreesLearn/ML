ll binpow (ll a, ll b){
    if (b == 0) return 1;
    if (b & 1) return ((binpow(a, b - 1)) * a);
    else return (binpow(a, b / 2) * binpow(a, b / 2));
}
ll gcd(ll a, ll b){
    return (b ? gcd(b, a % b) : a);
}
ll nums(ll g){
    ll cur = 0;
    while(g){
        cur++, g /= 10;
    }

    return cur;
}

vector <ll> fact(ll n){
    ll i = 2;

    vector <ll> ans;

    ll save = n;

    while(i * i <= save){
        while(n % i == 0){
            ans.pb(i);

            n /= i;
        }

        i++;
    }

    if (n > 1) ans.pb(n);

    return ans;
}

ll get1(ll q, ll g){
    ll cur = 1;

    for (ll i = q; i > g; i--){
        cur *= i;
    }

    return cur;
}
