int digits(ll n)
{
    int k = 0;
    while (n > 0)
    {
        n = n / 10;
        k++;
    }
    return k;
}

ll gcd(ll a, ll b)
{
    if (b == 0)
        return a;
    else
        return gcd(b, a % b);
}

ll lcm(ll a, ll b)
{
    return (a * b) / gcd(a, b);
}

ll power(ll a, ll b)
{
    ll sum = 1;
    while (b > 0)
    {
        sum = sum * a;
        b--;
    }
    return sum;
}

ll ncr(ll n, ll r)
{
    ll ans = 1;
    for (ll i = n; i > n - r; i--)
    {
        ans *= i;
    }
    for (ll i = r; i >= 1; i--)
    {
        ans /= i;
    }
    return ans;
}

ll nc2(ll n)
{
    return (n * (n - 1)) / 2;
}

