def printDivisors(n):
    # Note that this loop runs till square root
    i = 1
    res = []
    while i <= math.sqrt(n):

        if (n % i == 0):

            # If divisors are equal, print only one
            if (n / i == i):
                res.append(i)
            else:
                # Otherwise print both
                res.append(i)
                res.append(n // i)
        i = i + 1
    return res

