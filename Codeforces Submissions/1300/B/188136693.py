def fact(n):
    res = 1

    for i in range(2, n + 1):
        res = res * i

    return res

def SieveOfEratosthenes(n):
    prime = [True for i in range(n + 1)]
    p = 2
    while (p * p <= n):
        if (prime[p] == True):
            for i in range(p * p, n + 1, p):
                prime[i] = False
        p += 1
    ans = []
    for p in range(2, n + 1):
        if prime[p]:
            ans.append(p)
    return ans


def nextPowerOf2(n):
    count = 0
    if (n and not (n & (n - 1))):
        return n

    while (n != 0):
        n >>= 1
        count += 1

    return 1 << count


