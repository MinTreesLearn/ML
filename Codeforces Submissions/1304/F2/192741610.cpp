ll  seg[60][4*N],lazy[60][4*N] , a[60][N] ,dp[60][N],pre[60][N];
void push(int g,int p){
    if(!lazy[g][p]) return;
    seg[g][p<<1] += lazy[g][p]; seg[g][p<<1|1] += lazy[g][p];
    lazy[g][p<<1] += lazy[g][p]; lazy[g][p<<1|1] +=lazy[g][p];
    lazy[g][p] = 0;
}

ll upd(int g,int i , int j , int inc , int p , int l , int r){
    if(j<l || r<i) return seg[g][p];
    if(i<=l && r<=j) return lazy[g][p] += inc, seg[g][p] += inc;
    push(g,p);
    return seg[g][p] = max(upd(g,i , j , inc , left) , upd(g,i , j , inc , right));
}


 
